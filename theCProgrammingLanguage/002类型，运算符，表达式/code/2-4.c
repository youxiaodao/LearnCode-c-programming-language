/* 重新编写squeeze(s1,s2), 将字符串s1中任何与字符串s2中的字符匹配的字符都删除。*/
void squeeze(char s1[], char s2[]) {
    int i, j, k;
    for (i=k=0;s1[i] != '\0'; i++) {
        for (j = 0;s2[j] != '\0' && s2[j] != s1[i]; j++) {
            ;
        }  // 停止遍历, 代表出现了一个相等的
        if (s2[j] == '\0') { // 如果是遍历到了s2的最后一个, 就将s1[i] 复制到结果字符串中, 如果么有到结尾就结束了,说明由重复的，就将其剔除了
            s1[k++] = s1[i];
        }
    }
    s1[k] = '\0';
}